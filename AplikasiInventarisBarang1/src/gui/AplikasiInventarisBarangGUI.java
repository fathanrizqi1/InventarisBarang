    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Mendapatkan model dari tabel yang ada
        DefaultTableModel model = (DefaultTableModel) tbCatat.getModel();
        // Pengecekan apakah kolom input kosong
        if (tfNo.getText () . trim() .equals("") || tfMerek.getText () . trim() .equals("") || tfUkuran.getText () . trim() .equals("")){
        // Menampilkan pesan peringatan jika ada kolom yang kosong   
            JOptionPane.showMessageDialog(null,"Catatan yang anda isi kurang lengkap \n silahkan isi dengan lengkap");      
        }else{
            model.addRow(new Object[]{tfNo.getText(), cbJe.getSelectedItem(), tfMerek.getText(), tfUkuran.getText(), cbTipe.getSelectedItem(),cbSpek.getSelectedItem()});
            // Set ulang nilai input menjadi default atau kosong
            tfNo.setText("");
            cbJe.setSelectedItem("MOTOR");
            tfMerek.setText("");
            tfUkuran.setText("");
            cbTipe.setSelectedItem("TUBLES");
            cbSpek.setSelectedItem("SOFT COMPOUNT");
         // Tampilkan pesan konfirmasi bahwa catatan telah berhasil diinput
            JOptionPane.showMessageDialog(null,"Catatan anda berhasil di input");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

