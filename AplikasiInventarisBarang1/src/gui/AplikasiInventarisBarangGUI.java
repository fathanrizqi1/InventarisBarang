    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Mendapatkan model dari tabel yang ada  
        DefaultTableModel model = (DefaultTableModel) tbCatat.getModel();
        // Memeriksa apakah tidak ada baris yang dipilih pada tabel  
        if (tbCatat.getSelectedRow ()==-1){
            // Memeriksa jika baris yang dipilih adalah baris pertama (indeks ke-0)
              if (tbCatat.getSelectedRow()==0){
                  // Menampilkan pesan jika pengguna belum memilih catatan 
                  JOptionPane.showMessageDialog(null, "anda belum memilih catatan");
              }else{
                  // Menampilkan pesan untuk memilih catatan jika kondisi sebelumnya tidak terpenuhi 
                  JOptionPane.showMessageDialog(null, "silahkan pilih catatan");
              }
          }else{
              // Mengatur nilai komponen antarmuka pengguna (UI) dengan nilai dari baris yang dipilih pada tabel 
              tfNo.setText(model.getValueAt(tbCatat.getSelectedRow(), 0) .toString());
              cbJe.setSelectedItem(model.getValueAt(tbCatat.getSelectedRow(),1) .toString());
              tfMerek.setText(model.getValueAt(tbCatat.getSelectedRow(), 2).toString());
              tfUkuran.setText(model.getValueAt(tbCatat.getSelectedRow(), 3) .toString());
              cbTipe.setSelectedItem(model.getValueAt(tbCatat.getSelectedRow(),4) .toString());
              cbSpek.setSelectedItem(model.getValueAt(tbCatat.getSelectedRow(),1) .toString());   
          }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // Mendapatkan model tabel dari komponen tbCatat
        DefaultTableModel model = (DefaultTableModel) tbCatat.getModel();
        // Memeriksa apakah ada baris yang dipilih pada tabel
        if (tbCatat.getSelectedRow() ==-1) {
            // Memeriksa apakah baris pertama dipilih, jika belum, tampilkan pesan yang sesuai
            if (tbCatat.getSelectedRow()==0){
                JOptionPane.showMessageDialog(null, "pilih catatan yang ingin anda hapus");
            }else{
                JOptionPane.showMessageDialog(null, "pilih catatan yang di hapus");
            }
        }else{
            // Jika baris dipilih, hapus baris tersebut dari tabel
            model.removeRow(tbCatat.getSelectedRow());
            JOptionPane.showMessageDialog(null, "Data catatan berhasil dihapus");
        }
        // Setelah operasi hapus selesai, atur ulang nilai-nilai beberapa komponen UI ke nilai default atau kosong 
        tfNo.setText("");
        cbJe.setSelectedItem("sudah di pilih");
        tfMerek.setText("");
        tfUkuran.setText("");
        cbTipe.setSelectedItem("tipe berhasil di pilih");
        cbSpek.setSelectedItem("spek berhasil di pilih");
        tfNo.requestFocus(); // Memindahkan fokus ke field tfNo untuk input selanjutnya

        
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        try {
            // Membuat objek BufferedWriter untuk menulis ke file "ExportData.txt"
            BufferedWriter bw = new BufferedWriter(new FileWriter("ExportData.txt"));
            
            // Iterasi melalui setiap baris dan kolom tabel untuk mengekspor data ke file teks
            for(int i = 0; i < tbCatat.getRowCount(); i++) { //membaca rows table
                for(int j = 0; j < tbCatat.getColumnCount(); j++) { //membaca column table
                    bw.write(tbCatat.getValueAt(i, j).toString()+" ");// Menulis nilai sel ke file
                }
                bw.newLine();// Menambahkan baris baru setelah setiap baris tabel
            }
           // Menampilkan pesan konfirmasi jika proses ekspor berhasil 
           JOptionPane.showMessageDialog(null, "Data Berhasil Export");
           bw.close(); // Menutup BufferedWriter setelah selesai penggunaan
        } catch (Exception e) {
            System.err.println("Error: " + e.getMessage());// Menangani kesalahan jika terjadi
        }
    }//GEN-LAST:event_jButton7ActionPerformed
     


